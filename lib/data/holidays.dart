/// 한국 공휴일 데이터.
///
/// - 매년 같은 양력 공휴일은 [_fixed]로 자동 처리.
/// - 음력 기반(설날·추석·부처님오신날) 및 대체공휴일은 연도가 바뀌면 달라지므로
///   [_byDate]에 연도별로 등록한다. **매년 새 연도를 추가/검증**해 주세요.
class Holidays {
  const Holidays._();

  /// 매년 동일한 양력 공휴일. key = 월*100 + 일.
  static const Map<int, String> _fixed = {
    101: '신정',
    301: '삼일절',
    505: '어린이날',
    606: '현충일',
    815: '광복절',
    1003: '개천절',
    1009: '한글날',
    1225: '성탄절',
  };

  /// 음력 기반 + 대체공휴일 (yyyy-MM-dd). ※ 매년 검증·추가 필요.
  static const Map<String, String> _byDate = {
    // ===== 2026 =====
    '2026-02-16': '설날 연휴',
    '2026-02-17': '설날',
    '2026-02-18': '설날 연휴',
    '2026-03-02': '대체공휴일', // 삼일절(일)
    '2026-05-24': '부처님오신날',
    '2026-05-25': '대체공휴일', // 부처님오신날(일)
    '2026-08-17': '대체공휴일', // 광복절(토)
    '2026-09-24': '추석 연휴',
    '2026-09-25': '추석',
    '2026-09-26': '추석 연휴',
    '2026-09-28': '대체공휴일', // 추석연휴(토)
    '2026-10-05': '대체공휴일', // 개천절(토)

    // ===== 2027 (검증 필요) =====
    '2027-02-05': '설날 연휴',
    '2027-02-06': '설날',
    '2027-02-07': '설날 연휴',
    '2027-02-08': '대체공휴일',
    '2027-05-13': '부처님오신날',
    '2027-08-16': '대체공휴일', // 광복절(일)
    '2027-09-14': '추석 연휴',
    '2027-09-15': '추석',
    '2027-09-16': '추석 연휴',
    '2027-10-04': '대체공휴일', // 개천절(일)
    '2027-10-11': '대체공휴일', // 한글날(토)
    '2027-12-27': '대체공휴일', // 성탄절(토)
  };

  static String _key(DateTime d) =>
      '${d.year.toString().padLeft(4, '0')}-'
      '${d.month.toString().padLeft(2, '0')}-'
      '${d.day.toString().padLeft(2, '0')}';

  /// 해당 날짜의 공휴일 이름(없으면 null).
  static String? nameFor(DateTime d) {
    return _fixed[d.month * 100 + d.day] ?? _byDate[_key(d)];
  }

  static bool isHoliday(DateTime d) => nameFor(d) != null;
}
